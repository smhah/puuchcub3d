#include "cub3d.h"

void	check_info(char *s, int e)
{
	int j;
	int i;

	i = 0;
	while (s[i] == ' ' || s[i] == '\t')
		i++;
	j = 0;
	while (s[i])
		g_read[e][j++] = s[i++];
	g_read[e][j] = '\0';
}
